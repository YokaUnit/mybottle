export function getCompanyDocument(company: LegalCompanyInfo): LegalDocument {
  const sections: LegalSection[] = [
    {
      title: "サービス概要",
      body: [
        `${company.serviceName} は、飲食店におけるボトルキープをスマートフォンで管理・提示できるデジタルボトルキープサービスです。`,
        "店舗の既存オペレーションを大きく変えず、来店促進とリピート支援を目的としています。",
      ],
    },
    {
      title: "事業者情報",
      body: [
        `事業者名：${company.operatorName}`,
        `運営責任者：${company.representative}`,
        `所在地：${company.address}`,
        `電話番号：${company.phone}`,
        `メール：${company.email}`,
        `受付時間：${company.businessHours}`,
      ],
    },
  ];

  if (company.established) {
    sections.push({
      title: "設立",
      body: [company.established],
    });
  }
  if (company.corporateNumber) {
    sections.push({
      title: "法人番号",
      body: [company.corporateNumber],
    });
  }

  sections.push({
    title: "関連ページ",
    body: ["各種法的表示は以下のページをご確認ください。"],
    list: [
      "利用規約",
      "プライバシーポリシー",
      "特定商取引法に基づく表記",
      "Cookieポリシー",
      "お問い合わせ",
    ],
  });

  return {
    title: "会社概要",
    description: `${company.serviceName} の運営情報です。`,
    sections,
  };
}

